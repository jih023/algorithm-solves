n, m = map(int, input().split())
numbers = [list(map(int, input().split())) for _ in range(n)]

tetromino_shapes = [
    # ㅡ
    [(0,0),(0,1),(0,2),(0,3)],
    # ㅣ
    [(0,0),(1,0),(2,0),(3,0)],
    # ㅁ
    [(0,0),(0,1),(1,0),(1,1)],
    # L, J 모양
    [(0,0),(1,0),(2,0),(2,1)],
    [(0,1),(1,1),(2,1),(2,0)],
    [(0,0),(0,1),(1,0),(2,0)],
    [(0,0),(0,1),(1,1),(2,1)],
    [(0,0),(0,1),(0,2),(1,0)],
    [(0,0),(0,1),(0,2),(1,2)],
    [(0,0),(1,0),(1,1),(1,2)],
    [(0,2),(1,0),(1,1),(1,2)],
    # S, Z 모양
    [(0,0),(0,1),(1,1),(1,2)],
    [(0,1),(0,2),(1,0),(1,1)],
    [(0,0),(1,0),(1,1),(2,1)],
    [(0,1),(1,0),(1,1),(2,0)],
    # ㅗ 모양
    [(0,0),(0,1),(0,2),(1,1)],
    [(0,1),(1,0),(1,1),(2,1)],
    [(1,0),(1,1),(1,2),(0,1)],
    [(0,0),(1,0),(1,1),(2,0)]
]

max_num = 0

for i in range(n):
    for j in range(m):
        for shape in tetromino_shapes:
            try:
                total = sum(numbers[i+dx][j+dy] for dx, dy in shape)
                max_num = max(max_num, total)
            except IndexError:
                continue

print(max_num)